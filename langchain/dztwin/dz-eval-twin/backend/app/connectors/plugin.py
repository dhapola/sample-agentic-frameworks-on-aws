"""Application connector plugin interface and base classes.

This module defines the plugin architecture for connecting to various
generative AI applications. Plugins provide a generic interface for
sending inputs and receiving responses from different application types.
"""

from abc import ABC, abstractmethod
from dataclasses import dataclass
from typing import Any, Dict, Optional

from app.models.connection_config import ConnectionConfig


@dataclass
class ApplicationResponse:
    """
    Response from a generative AI application.
    
    Captures the output, latency, and any metadata or errors
    from an application's response to an input.
    
    Attributes:
        output: The text output generated by the application
        latency: Response time in milliseconds
        metadata: Optional additional response metadata
        error: Optional error message if the request failed
    """
    output: str
    latency: float
    metadata: Optional[Dict[str, Any]] = None
    error: Optional[str] = None


class ApplicationPlugin(ABC):
    """
    Abstract base class for application connector plugins.
    
    Defines the interface that all connector plugins must implement
    to integrate with different types of generative AI applications.
    Each plugin handles the specifics of connecting to and communicating
    with a particular application type or protocol.
    
    Attributes:
        type: The plugin type identifier (e.g., 'http', 'websocket')
        config: Connection configuration for the application
        _connected: Internal connection state flag
    """
    
    def __init__(self, plugin_type: str):
        """
        Initialize the plugin with a type identifier.
        
        Args:
            plugin_type: String identifier for the plugin type
        """
        self.type = plugin_type
        self.config: Optional[ConnectionConfig] = None
        self._connected: bool = False
    
    @abstractmethod
    async def connect(self, config: ConnectionConfig) -> None:
        """
        Establish connection to the generative AI application.
        
        This method should initialize any necessary connections,
        authenticate if required, and prepare the plugin to send inputs.
        
        Args:
            config: Connection configuration including endpoint,
                   authentication, timeout, and retry settings
        
        Raises:
            ConnectionError: If connection cannot be established
            ValueError: If configuration is invalid
        """
        pass
    
    @abstractmethod
    async def disconnect(self) -> None:
        """
        Close connection to the generative AI application.
        
        This method should clean up any resources, close connections,
        and reset the plugin state.
        
        Raises:
            RuntimeError: If disconnection fails
        """
        pass
    
    @abstractmethod
    async def send_input(self, input_text: str) -> ApplicationResponse:
        """
        Send input to the application and receive response.
        
        This method sends the input text to the connected application,
        measures the latency, and returns the response with metadata.
        
        Args:
            input_text: The input text to send to the application
        
        Returns:
            ApplicationResponse containing output, latency, and metadata
        
        Raises:
            RuntimeError: If not connected or if request fails
            TimeoutError: If request exceeds configured timeout
        """
        pass
    
    @abstractmethod
    def is_connected(self) -> bool:
        """
        Check if the plugin is currently connected.
        
        Returns:
            True if connected and ready to send inputs, False otherwise
        """
        pass


class BaseApplicationPlugin(ApplicationPlugin):
    """
    Base implementation of ApplicationPlugin with common functionality.
    
    Provides default implementations for connection state management
    and common validation logic. Concrete plugins can extend this class
    to inherit shared functionality while implementing protocol-specific
    communication logic.
    """
    
    def __init__(self, plugin_type: str):
        """
        Initialize the base plugin.
        
        Args:
            plugin_type: String identifier for the plugin type
        """
        super().__init__(plugin_type)
    
    async def connect(self, config: ConnectionConfig) -> None:
        """
        Store configuration and mark as connected.
        
        Subclasses should override this method to add actual connection logic,
        calling super().connect(config) to store the configuration.
        
        Args:
            config: Connection configuration
        
        Raises:
            ValueError: If config is None
        """
        if config is None:
            raise ValueError("Connection configuration cannot be None")
        
        self.config = config
        self._connected = True
    
    async def disconnect(self) -> None:
        """
        Reset connection state.
        
        Subclasses should override this method to add cleanup logic,
        calling super().disconnect() to reset the state.
        """
        self._connected = False
        self.config = None
    
    def is_connected(self) -> bool:
        """
        Check connection state.
        
        Returns:
            True if connected, False otherwise
        """
        return self._connected
    
    def _validate_connected(self) -> None:
        """
        Validate that the plugin is connected before sending input.
        
        Raises:
            RuntimeError: If not connected
        """
        if not self.is_connected():
            raise RuntimeError(
                f"{self.type} plugin is not connected. "
                "Call connect() before sending input."
            )
    
    def _validate_input(self, input_text: str) -> None:
        """
        Validate input text before sending.
        
        Args:
            input_text: The input text to validate
        
        Raises:
            ValueError: If input is None or empty
        """
        if input_text is None:
            raise ValueError("Input text cannot be None")
        if not isinstance(input_text, str):
            raise ValueError("Input text must be a string")
        if not input_text.strip():
            raise ValueError("Input text cannot be empty")
