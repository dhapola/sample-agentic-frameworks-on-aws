"""Response model for gen AI application outputs."""

from datetime import datetime
from typing import Optional

from pydantic import BaseModel, Field, field_validator

from .metrics import IndividualMetrics


class Response(BaseModel):
    """
    Response from gen AI application for a test case.
    
    Represents the output generated by a generative AI application
    for a given test case input, including timing and quality metrics.
    """
    
    test_case_id: str = Field(..., description="ID of the test case this response is for")
    input: str = Field(..., description="Input text sent to the application")
    output: str = Field(..., description="Output text received from the application")
    latency: float = Field(..., ge=0.0, description="Response latency in milliseconds")
    timestamp: datetime = Field(
        default_factory=datetime.utcnow,
        description="Timestamp when response was received"
    )
    error: Optional[str] = Field(
        default=None,
        description="Error message if the request failed"
    )
    individual_metrics: Optional[IndividualMetrics] = Field(
        default=None,
        description="Quality metrics for this response"
    )
    
    @field_validator('test_case_id')
    @classmethod
    def validate_test_case_id(cls, v: str) -> str:
        """Validate test case ID is not empty."""
        if not v or not v.strip():
            raise ValueError("Test case ID cannot be empty")
        return v.strip()
    
    @field_validator('input')
    @classmethod
    def validate_input(cls, v: str) -> str:
        """Validate input is not empty."""
        if not v or not v.strip():
            raise ValueError("Input cannot be empty")
        return v
    
    @field_validator('latency')
    @classmethod
    def validate_latency(cls, v: float) -> float:
        """Validate latency is non-negative."""
        if v < 0.0:
            raise ValueError("Latency cannot be negative")
        return v
    
    @field_validator('output')
    @classmethod
    def validate_output(cls, v: str, info) -> str:
        """Validate output based on error status."""
        # If there's an error, output can be empty
        # Otherwise, we allow any output including empty strings
        return v
    
    class Config:
        """Pydantic model configuration."""
        json_schema_extra = {
            "example": {
                "test_case_id": "tc_001",
                "input": "What is the capital of France?",
                "output": "The capital of France is Paris.",
                "latency": 245.5,
                "timestamp": "2024-01-01T12:00:00Z",
                "error": None,
                "individual_metrics": {
                    "accuracy": 0.95,
                    "relevance": 0.88
                }
            }
        }
